CREATE TABLE "OFFENCE_RESULT_CODES"
(
  "RESULT_CODE"                   VARCHAR2(6 CHAR)                  NOT NULL ,
  "DESCRIPTION"                   VARCHAR2(240 CHAR)                NOT NULL ,
  "DISPOSITION_CODE"              VARCHAR2(12 CHAR)                 NOT NULL ,
  "CHARGE_STATUS"                 VARCHAR2(12 CHAR)                 NOT NULL ,
  "ACTIVE_FLAG"                   VARCHAR2(1 CHAR) DEFAULT 'N',
  "EXPIRY_DATE"                   DATE,
  "CREATE_USER_ID"                VARCHAR2(40 CHAR) DEFAULT USER    NOT NULL ,
  "CREATE_DATETIME"               TIMESTAMP(9) DEFAULT systimestamp NOT NULL ,
  "MODIFY_USER_ID"                VARCHAR2(40 CHAR),
  "MODIFY_DATETIME"               TIMESTAMP(9),
  "CONVICTION_FLAG"               VARCHAR2(1 CHAR) DEFAULT 'N',
  "AUDIT_TIMESTAMP"               TIMESTAMP(9),
  "AUDIT_USER_ID"                 VARCHAR2(32 CHAR),
  "AUDIT_MODULE_NAME"             VARCHAR2(65 CHAR),
  "AUDIT_CLIENT_USER_ID"          VARCHAR2(64 CHAR),
  "AUDIT_CLIENT_IP_ADDRESS"       VARCHAR2(39 CHAR),
  "AUDIT_CLIENT_WORKSTATION_NAME" VARCHAR2(64 CHAR),
  "AUDIT_ADDITIONAL_INFO"         VARCHAR2(256 CHAR),
  "LIST_SEQ"                      NUMBER(6, 0),
  CONSTRAINT "OFFENCE_RESULT_CODES_PK" PRIMARY KEY ("RESULT_CODE")
);


COMMENT ON COLUMN "OFFENCE_RESULT_CODES"."RESULT_CODE" IS 'Result codes used to record offence outcomes at each court appearance';

COMMENT ON COLUMN "OFFENCE_RESULT_CODES"."DESCRIPTION" IS 'Description of the results.';

COMMENT ON COLUMN "OFFENCE_RESULT_CODES"."DISPOSITION_CODE" IS 'disposition code .';

COMMENT ON COLUMN "OFFENCE_RESULT_CODES"."CHARGE_STATUS" IS 'Charge status.';

COMMENT ON COLUMN "OFFENCE_RESULT_CODES"."ACTIVE_FLAG" IS 'Whether result is active or not';

COMMENT ON COLUMN "OFFENCE_RESULT_CODES"."EXPIRY_DATE" IS 'Expiry date';

COMMENT ON COLUMN "OFFENCE_RESULT_CODES"."CREATE_USER_ID" IS 'The user who creates the record';

COMMENT ON COLUMN "OFFENCE_RESULT_CODES"."CREATE_DATETIME" IS 'The timestamp when the record is created';

COMMENT ON COLUMN "OFFENCE_RESULT_CODES"."MODIFY_USER_ID" IS 'The user who modifies the record';

COMMENT ON COLUMN "OFFENCE_RESULT_CODES"."MODIFY_DATETIME" IS 'The timestamp when the record is modified ';

COMMENT ON COLUMN "OFFENCE_RESULT_CODES"."LIST_SEQ" IS 'The listing order sequence';

COMMENT ON TABLE "OFFENCE_RESULT_CODES" IS 'The possible result codes of offences';


